public class ReplicationDegree {

    private double repDegDouble;
    private int repDegInt;

    public ReplicationDegree(double repDegDouble, int repDegInt){

        this.repDegDouble = repDegDouble;
        this.repDegInt = repDegInt;
    }

    public void setRepDegDouble(double repDegDouble){

        this.repDegDouble = repDegDouble;
    }
    public double getRepDegDouble(){

        return this.repDegDouble;
    }

    public void setRepDegInt(int repDegInt){

        this.repDegInt = repDegInt;
    }

    public int getRepDegInt(){

        return this.repDegInt;
    }
}
